#To illustrate String trimming equivalent Strip() function in Python.

#Strip() function removes the whitespaces from the specified string
#including whitespaces.

#lstrip() function removes whitespaces to the left of the specified String.

#rstrip() function removes whitespaces to the right of the specified String.

s1= '   Hi   '

print(s1.strip())    #strips whitespaces on both left and right side of the string.
print(s1.lstrip())   #strips whitespaces from only the left side of the string.
print(s1.rstrip())    #strips whitespaces from only the right side of the string.
