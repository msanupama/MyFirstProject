#Let's write a program to show different ways of creating a
# string in Python

#First Method : With a pair of single quotes

my_str='Hello'
print(my_str)

#Second Method : With a pair of double quotes

my_str="Hello"
print(my_str)

#Third MEthod : With a pair of triple quotes

my_str='''Hello'''
print(my_str)

#Fourth Method : With 3 pairs of double quotes
#This method is generally used to create docstrings for
#documentation purposes.This method is used to create
#multiline strings at a time.

my_str="""Hello"""
print(my_str)
